window.I18N_CURRENT = localStorage.getItem('ls_lang') || 'zh-CN';

window.t = function(key, ...args) {
  let str = (window.I18N[window.I18N_CURRENT] || window.I18N['zh-CN'])[key] || key;
  args.forEach((a, i) => { str = str.replace('{' + i + '}', a); });
  return str;
};

window.applyI18n = function(lang) {
  if (lang) window.I18N_CURRENT = lang;
  localStorage.setItem('ls_lang', window.I18N_CURRENT);
  document.documentElement.lang = window.I18N_CURRENT;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = window.t(el.getAttribute('data-i18n'));
    if (val) el.textContent = val;
  });
  const ll = document.getElementById('langLabel');
  if (ll) ll.textContent = window.t('langLabel');
  document.querySelectorAll('.lang-option').forEach(o => {
    o.classList.toggle('active', o.dataset.lang === window.I18N_CURRENT);
  });
};

document.addEventListener('DOMContentLoaded', () => window.applyI18n());
